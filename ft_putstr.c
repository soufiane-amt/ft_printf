#include<unistd.h>

void	ft_putstr(char *str, int *re)
{
	int	j;

	j = 0;
	if (str == NULL)
	{
		write(1, "(null)", 6);
		*re += 6;
		return ;
	}
	while (*(str + j) != '\0')
	{
		write(1, str + j, 1);
		j++;
	}
	*re += j;
}
